function temaOriginal() {
    document.body.classList.remove("dark")
    document.body.classList.remove("azul")
}

function temaDark() {
    document.body.classList.add("dark")
    document.body.classList.remove("azul")
}

function temaAzul() {
    document.body.classList.add("azul")
    document.body.classList.remove("dark")
}